    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            s = con.createStatement();
            String idv = idvalues.getText();
            String a = null,b = null,c = null;
            String sub = (String) subval.getSelectedItem();
            String sql1 = "select "+sub+"_marks from cat1 where student_id= "+idv;
            String sql2 = "select "+sub+"_marks from cat2 where student_id= "+idv;
            String sql3 = "select "+sub+"_marks from fat where student_id= "+idv;
            rs1 = s.executeQuery(sql1);
            if(rs1.next()){
                a = rs1.getString(1);
                c1.setText(a);
            }
            rs2 = s.executeQuery(sql2);
            if(rs2.next()){
                b = rs2.getString(1);
               c2.setText(b);
            }
            rs3 = s.executeQuery(sql3);
            if(rs3.next()){
                c = rs3.getString(1);
                f.setText(c);
            }
            int av = Integer.parseInt(a);
            int bv = Integer.parseInt(b);
            int cv = Integer.parseInt(c);
            
            String fin = Float.toString((av+bv+cv)/3);
            finval.setText(fin);
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

